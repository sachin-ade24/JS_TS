/*do..while loop*/

//1.
let k=1;
do{
    console.log(k);
    k++;
}while(k<=10);// --> This will log from 1 to 10
console.log(k);//11

console.log('======================================================================================');

//2.
let j=1;
do{
    j++;
    console.log(j);
}while(j<=10); // --> This will log from 2 to 11
console.log(j);//11


//Negative do..while loop
let m = 10;
do{
    console.log(m);
    m--;
}while(m>=1);
console.log(m);

